-- 支援 AI 行程推薦（地點、天氣、行程、價格）

-- 清除現有表格（若存在）
DROP TABLE IF EXISTS cruise_facts;
DROP TABLE IF EXISTS weather;
DROP TABLE IF EXISTS itineraries;
DROP TABLE IF EXISTS locations;

-- 建立 locations 表（城市資訊）
CREATE TABLE locations (
    location_id TEXT PRIMARY KEY,
    city TEXT NOT NULL,
    country TEXT NOT NULL
);

-- 建立 itineraries 表（郵輪行程）
CREATE TABLE itineraries (
    itinerary_id TEXT PRIMARY KEY,
    url TEXT NOT NULL,
    destination TEXT NOT NULL,
    itinerary_name TEXT NOT NULL,
    rating REAL,
    review_count INTEGER,
    participant_count INTEGER,
    min_price_ntd INTEGER,
    location_id TEXT,
    FOREIGN KEY (location_id) REFERENCES locations(location_id)
);

-- 建立 weather 表（天氣資料）
CREATE TABLE weather (
    weather_id TEXT PRIMARY KEY,
    location_id TEXT NOT NULL,
    year INTEGER NOT NULL,
    month INTEGER NOT NULL,
    average_temperature REAL,
    precipitation_mm REAL,
    FOREIGN KEY (location_id) REFERENCES locations(location_id)
);

-- 建立 cruise_facts 表（MSC 航程售價）
CREATE TABLE cruise_facts (
    cruise_id TEXT PRIMARY KEY,
    ship_name TEXT NOT NULL,
    cabin_type TEXT NOT NULL,
    cabin_count INTEGER,
    cabin_size_sqm REAL,
    price_per_person_ntd INTEGER,
    departure_date TEXT,
    duration_days INTEGER,
    tour_type TEXT
);

